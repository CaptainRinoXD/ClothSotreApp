        EmID_TextField.setBackground(Color.WHITE);
        Bill_Total_TextField.setBackground(Color.WHITE);
        Caddress_TextField.setBackground(Color.WHITE);
        try{
            BufferedImage bi = new BufferedImage(jPanel1.getWidth(), jPanel1.getHeight(), BufferedImage.TYPE_INT_RGB);
            // Lỗi khi sử dụng BufferedImage.TYPE_INT_ARGB thay vì BufferedImage.TYPE_INT_RGB
            jPanel1.paint(bi.getGraphics());
            String path = "Bill.jpg";
            ImageIO.write(bi,"jpg", new File(path));
            Desktop.getDesktop().print(new File(path));
        } catch (Exception exc) {
            JOptionPane.showMessageDialog(this, "Error:" + exc.getMessage(), "Error",  JOptionPane.ERROR_MESSAGE);
            JOptionPane.showMessageDialog(this, "Print Error: " + exc.getMessage(), "Print Error", JOptionPane.ERROR_MESSAGE);
        }
        btnPrint.setVisible(true);
