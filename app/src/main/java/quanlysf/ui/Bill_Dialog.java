    private void populateFormFields() {
        Bill_ID_TextField.setText(String.valueOf(bill.getBill_ID()));
        ExportDate_TextField.setText(String.valueOf(bill.getExpDate()));

        int CID = bill.getCID();
        int EmID = bill.getEmID();
        try {
            List<Employee> employees = employeeDAO.get(EmID);
            Employee employee = employees.get(0);
            String EmployeeName = employee.getName();
            EmID_TextField.setText(EmployeeName);

            List<Costumer> costumers = costumerDAO.get(CID);
            Costumer costumer = costumers.get(0);
            CName_TextField.setText(costumer.getCname());
            CPhone_TextField.setText(String.valueOf(costumer.getCnumber()));
            Caddress_TextField.setText(costumer.getCaddress());

            detailBills = detailBillDAO.getlong(bill.getBill_ID());
            if (detailBills.isEmpty()){
                return;
            }
            DetailBill_Tb_Model dialog = new DetailBill_Tb_Model(detailBills);
            jTable1.setModel(dialog);

            long totalProductTotal = 0;
            for (DetailBill detailBill : detailBills) {
                totalProductTotal += detailBill.getBill_Total();
            }
            Bill_Total_TextField.setText(String.valueOf(totalProductTotal));
        } catch (Exception exc) {
            exc.printStackTrace();
        }
        
    }

